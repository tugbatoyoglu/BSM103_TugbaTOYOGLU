import java.util.*;
import java.io.*;
public class ORNEK3 {

		private int x;
		private int y;
		
		public ORNEK3(int x , int y) {
			this.x = x;
			this.y = y;
		}
		public int getX() {
			return x;
		}
		public int getY() {
			return y;
		}
		public void setX(int x) {
			this.x = x;
		}
		public void setY(int y) {
			this.y = y;
		}

	}

